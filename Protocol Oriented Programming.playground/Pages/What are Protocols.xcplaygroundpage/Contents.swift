# What are Protocols
### *Dictionary Definition*
> the accepted or established code of procedure or behavior in any group, organization, or situation

### *Swift Programming Language Definition*
> A protocol defines a blueprint of methods, properties, and other requirements that suit a particular task or piece of functionality. The protocol can then be adopted by a class, structure, or enumeration to provide an actual implementation of those requirements. Any type that satisfies the requirements of a protocol is said to conform to that protocol.

#### So, a contract that a type can conform to so, that other code knows how to interact with it

